package first;

// This program will display the area and perimeter of a rectangle with width of 4.5 and height of 7.9

public class AreaPerimeterRect {

	public static void main(String[] args) {
		
		double width = 4.5;
		double height = 7.9;
		double area = (width * height);
		
		System.out.print(area);
	}
}
